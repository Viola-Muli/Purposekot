fun main() {
    //Arithmetic operations
    var x = 90
    var y = 5
    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)//modulus[remainder]

    //Comparison operators
    var num1 = 45
    var num2 = 30
    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)

    //Assignment operators
    var number = 10
    println(number)

    number /= 2
    println(number)

    //Logic operators - &&[and, ||[or],!![not]
    println((100 > 20)&& (80 < 10))
    println((100 > 20)|| (80 < 10))
    println(!((100 > 20) || (80 < 10)))















}